    private void resetPanggilanAntrian() {
        if (Sequel.cariInteger("select count(-1) from antrian_pemanggil where date(waktu_panggil)=date(now())") > 0) {
            Sequel.menyimpan("antrian_history_reset", "'Panggilan Antrian',"
                    + "'" + Sequel.cariIsi("select no_antrian from antrian_pemanggil where date(waktu_panggil)=date(now()) order by no_antrian desc limit 1") + "',"
                    + "'" + Sequel.cariIsi("SELECT date(now())") + "'", "Nomor Antrian Panggilan");
            Sequel.queryu("delete from antrian_pemanggil where date(waktu_panggil) < DATE_FORMAT(date_sub(now(), interval 2 day),'%Y-%m-%d')");
        }
    }
    
    private void resetNomorAntrian() {
        if (Sequel.cariInteger("select count(-1) from antrian_nomor where date(waktu_cetak)=date(now())") > 0) {
            Sequel.menyimpan("antrian_history_reset", "'Nomor Antrian',"
                    + "'" + Sequel.cariIsi("select no_antrian from antrian_nomor where date(waktu_cetak)=date(now()) order by no_antrian desc limit 1") + "',"
                    + "'" + Sequel.cariIsi("SELECT date(now())") + "'", "Nomor Antrian Pasien");
            Sequel.queryu("delete from antrian_nomor where date(waktu_cetak) < DATE_FORMAT(date_sub(now(), interval 2 day),'%Y-%m-%d')");
        }
    }
    
