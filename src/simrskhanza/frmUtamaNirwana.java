    private void jam() {
        ActionListener taskPerformer = new ActionListener() {
            private int nilai_jam;
            private int nilai_menit;
            private int nilai_detik;

            public void actionPerformed(ActionEvent e) {
                String nol_jam = "";
                String nol_menit = "";
                String nol_detik = "";

                Date now = Calendar.getInstance().getTime();

                // Mengambil nilaj JAM, MENIT, dan DETIK Sekarang
                    nilai_jam = now.getHours();
                    nilai_menit = now.getMinutes();
                    nilai_detik = now.getSeconds();

                // Jika nilai JAM lebih kecil dari 10 (hanya 1 digit)
                if (nilai_jam <= 9) {
                    // Tambahkan "0" didepannya
                    nol_jam = "0";
                }
                // Jika nilai MENIT lebih kecil dari 10 (hanya 1 digit)
                if (nilai_menit <= 9) {
                    // Tambahkan "0" didepannya
                    nol_menit = "0";
                }
                // Jika nilai DETIK lebih kecil dari 10 (hanya 1 digit)
                if (nilai_detik <= 9) {
                    // Tambahkan "0" didepannya
                    nol_detik = "0";
                }
                // Membuat String JAM, MENIT, DETIK
                String jam = nol_jam + Integer.toString(nilai_jam);
                String menit = nol_menit + Integer.toString(nilai_menit);
                String detik = nol_detik + Integer.toString(nilai_detik);
                
                // Menampilkan pada Layar                
                tgljam.setText("Hari ini Tgl. " + Valid.SetTglINDONESIA(Sequel.cariIsi("SELECT DATE(NOW())")) + ", Pukul. " + jam + ":" + menit + ":" + detik + " WITA");
            }
        };
        // Timer
        new Timer(1000, taskPerformer).start();
    }
    
    private void persenTombol() {
        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
                //proses bar diatur 50%. nilai i < 51 hasilnya 50%
                for (int i = 0; i < 51; i++) {
                    try {
                        prosesTombol.setValue(i);
                        Thread.sleep(50);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(DlgNomorAntrian.class.getName()).log(Level.SEVERE, null, ex);                        
                    }
                }

                if (prosesTombol.getString().equals("50%")) {
                    cekTIDAK();
                }
            }
        });

        t.start();
    }
    
