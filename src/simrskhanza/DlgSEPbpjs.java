    private void cekkodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cekkodeActionPerformed
        if (kodeboking.getText().equalsIgnoreCase("BK333")) {
            int jawab = JOptionPane.showConfirmDialog(null, "Yakin anda mau keluar dari program ini...????", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (jawab == JOptionPane.YES_OPTION) {
                System.exit(0);
            }
        } else if (kodeboking.getText().equalsIgnoreCase("BK222")) {
            int coba_prin = JOptionPane.showConfirmDialog(null, "Yakin anda mau melakukan tes print..????", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (coba_prin == JOptionPane.YES_OPTION) {
                this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
                Map<String, Object> param = new HashMap<>();
                param.put("logo", Sequel.cariGambar("select logo from setting"));
                Valid.AutoPrint("rptTesPrint.jasper", "report", "::[ Tes Print Out ]::",
                        " SELECT DATE_FORMAT(NOW(),'Tgl. Cetak : %d-%m-%Y, Jam : %h:%i %p') waktu", param);
                this.setCursor(Cursor.getDefaultCursor());

                label_pesan.setVisible(true);
                data_pasien.setVisible(false);
                label_pesan.setText("Proses test print sedang berlangsung & hanya tercetak selembar aja...!!!");
            }
        } else {
            cetaksep.setEnabled(true);
            try {
                if (kodeboking.getText().trim().equals("")) {
                    label_pesan.setVisible(true);
                    data_pasien.setVisible(false);
                    label_pesan.setText("Kode Booking tidak boleh kosong..!!!");
                } else {
                    psCekBooking = koneksi.prepareStatement("select b.no_rkm_medis, b.kd_booking, DATE_FORMAT(b.tanggal_periksa, '%Y-%m-%d') tglPeriksa,p.nm_pasien,"
                            + "k.nm_poli,d.nm_dokter,s.no_kartu, b.antrian_khusus from booking_registrasi b "
                            + "inner join kelengkapan_booking_sep_bpjs s on s.kd_booking = b.kd_booking inner join pasien p on p.no_rkm_medis = b.no_rkm_medis "
                            + "inner join poliklinik k on k.kd_poli = b.kd_poli inner join dokter d on d.kd_dokter = b.kd_dokter "
                            + "where b.kd_booking ='" + kodeboking.getText() + "'");
                    rsCekBooking = psCekBooking.executeQuery();

                    if (rsCekBooking.next() == true) {
//                        while (rsCekBooking.next()) {
                        tglCek = rsCekBooking.getString("tglPeriksa");
                        nm_pasien.setText(rsCekBooking.getString("nm_pasien"));
                        cekKDboking.setText(rsCekBooking.getString("kd_booking"));
                        poli_tujuan.setText(rsCekBooking.getString("nm_poli"));
                        tgl_cek.setText(rsCekBooking.getString("tglPeriksa"));
                        nm_dokter.setText(rsCekBooking.getString("nm_dokter"));
                        norm.setText(rsCekBooking.getString("no_rkm_medis"));
                        noBpjs = rsCekBooking.getString("no_kartu");
                        antrian.setText(rsCekBooking.getString("antrian_khusus"));
                        
//                        }
                        data_pasien.setVisible(true);
                        label_pesan.setVisible(false);
                        cekBookingReg = Sequel.cariIsi("select status_booking from booking_registrasi where kd_booking='" + kodeboking.getText() + "'");
                        Sequel.cariIsi("select status_cetak_sep from kelengkapan_booking_sep_bpjs where kd_booking=?", cekStatusCetak, kodeboking.getText());
//                        cekRegFinger(noBpjs);
                        if (cekBookingReg.equals("Menunggu")) {
                            umurPasien();
                            if (prop.getProperty("HARUSFINGER").equals("YA")) {
                                if ((Integer.parseInt(umur) > 17 && sttsumur.equals("Th"))) {
                                    if (prop.getProperty("SKIPJIWA").equals("TIDAK")) {
                                        if (cekRegFinger(noBpjs) == false) {
                                            label_pesan.setVisible(true);
                                            data_pasien.setVisible(false);
                                            label_pesan.setText("Anda Belum Melakukan Registrasi Finger Print, Silakan Hubungi Petugas Informasi!!");
                                            reset.requestFocus();
                                            ya.setVisible(false);
                                            tidak.setVisible(false);
                                        } else {
                                            if (prop.getProperty("LOGINFINGER").equals("YA")) {
                                                if (cekInFinger(noBpjs, tglCek) == false) {
                                                    loginFinger();
                                                }
                                            }
                                        }
                                    } else {
                                        if (!poli_tujuan.getText().equals("JIWA")) {
                                            if (cekRegFinger(noBpjs) == false) {
                                                label_pesan.setVisible(true);
                                                data_pasien.setVisible(false);
                                                label_pesan.setText("Anda Belum Melakukan Registrasi Finger Print, Silakan Hubungi Petugas Informasi!!");
                                                reset.requestFocus();
                                                ya.setVisible(false);
                                                tidak.setVisible(false);
                                            } else {
                                                if (prop.getProperty("LOGINFINGER").equals("YA")) {
                                                    if (cekInFinger(noBpjs, tglCek) == false) {
                                                        loginFinger();
                                                    }
                                                }
                                            }
                                        }

                                    }
                                }
                            }
                        }
                    } else {
                        label_pesan.setVisible(true);
                        data_pasien.setVisible(false);
                        label_pesan.setText("Kode Booking tidak ditemukan, silakan ulangi lagi...!!");
                        reset.requestFocus();
                        ya.setVisible(false);
                        tidak.setVisible(false);
                    }
                }

//                if (kodeboking.getText().trim().equals("")) {
//                    label_pesan.setVisible(true);
//                    data_pasien.setVisible(false);
//                    label_pesan.setText("Kode Booking tidak boleh kosong..!!!");                    
//                } else if (cekKDboking.getText().equals("")) {
//                    label_pesan.setVisible(true);
//                    data_pasien.setVisible(false);
//                    label_pesan.setText("Kode Booking tidak ditemukan, silakan ulangi lagi...!!");                    
//                    reset.requestFocus();
//                }
            } catch (Exception e) {
                label_pesan.setText(e.toString());
                label_pesan.setVisible(true);
                data_pasien.setVisible(false);
            }
        }
    }//GEN-LAST:event_cekkodeActionPerformed

