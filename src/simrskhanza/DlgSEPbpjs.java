    private void cetaksepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetaksepActionPerformed
        cekDaftar = 0;
        cetaksep.setEnabled(false);
        antrianKhusus = "";

        if (tglCek.equals(Valid.SetTgl(tglPeriksa.getSelectedItem() + ""))) {
            cekPasienBoking();
//            nomorAuto();
            umurPasien();
            cekDaftar = Sequel.cariInteger("SELECT count(1) cek FROM booking_registrasi WHERE kd_booking='" + kodeboking.getText() + "' and no_rawat<>'-'");

            if (kdpenjab.getText().equals("B01") || (kdpenjab.getText().equals("A03"))) {
                if (cekDaftar >= 1) {
                    label_pesan.setText("Pasien dg. kode booking " + kodeboking.getText() + " telah terdaftar pada hari ini...!!!");

                    if (cekStatusCetak.getText().equals("SUDAH")) {
                        label_pesan.setVisible(true);
                        data_pasien.setVisible(false);
                        label_pesan.setText("Untuk rujukan internal antar poliklinik, silakan menuju loket pendaftaran, Apakah bukti registrasi pasien "
                                + "akan dicetak ulang..? tekan tombol YA untuk mencetak, tekan tombol TIDAK jika batal.");                        
                        ya.setVisible(true);
                        tidak.setVisible(true);
                    } else {
                        norwBoking.setText(Sequel.cariIsi("select no_rawat from booking_registrasi where kd_booking='" + kodeboking.getText() + "'"));
                        cetakSEPBPJSgagal();
                    }

                } else {
                    if (cekBookingReg.equals("Menunggu")) {
                        cekKelengkapanSEP();
                        if (simpanRegistrasi() == true) {
                            cekFingerPrin();
                        } else {
                            label_pesan.setText("Registrasi Gagal, Silakan Coba Lagi !!");
                        }

                    } else if (cekStatusCetak.getText().equals("GAGAL")) {
                        label_pesan.setVisible(true);
                        data_pasien.setVisible(false);
                        label_pesan.setText("Pasien dg. kode booking " + kodeboking.getText() + " telah membatalkan proses bookingnya, silakan mendaftar lagi keloket pendaftaran..!!!");
                    }
                }

            } else {
                label_pesan.setVisible(true);
                data_pasien.setVisible(false);
                label_pesan.setText("Untuk saat ini layanan Anjungan Elektronik Mandiri hanya untuk pasien BPJS saja...!!");
            }
        } else {
            data_pasien.setVisible(false);
            label_pesan.setVisible(true);
            label_pesan.setText("Tanggal Periksa Tidak Sama Dengan tanggal Hari Ini");
        }
    }//GEN-LAST:event_cetaksepActionPerformed

