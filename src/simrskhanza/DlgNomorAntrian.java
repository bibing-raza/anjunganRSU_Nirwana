    private void cekNoTerakhirUmum() {
        if (Sequel.cariInteger("select count(-1) from antrian_nomor_umum where date(waktu_cetak)=date(now())") < 1) {
            no_loket_umum.setText("Terakhir : 0");
        } else {
            try {
                ps1 = koneksi.prepareStatement("select max(no_antrian) from antrian_nomor_umum where date(waktu_cetak)=date(now())");
                try {
                    rs1 = ps1.executeQuery();
                    if (rs1.next()) {
                        no_loket_umum.setText("Terakhir : " + rs1.getString(1));
                    }
                } catch (Exception e) {
                } finally {
                    if (rs1 != null) {
                        rs1.close();
                    }

                    if (ps1 != null) {
                        ps1.close();
                    }
                }
            } catch (Exception e) {
            }
        }
    }

