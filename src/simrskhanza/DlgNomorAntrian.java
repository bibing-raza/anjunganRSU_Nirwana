    private void BtnCloseAppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCloseAppActionPerformed
        if (angka.getText().equals("")) {
            label_pesan.setText("Angka Pengaturan tidak boleh kosong, silahkan isi "
                    + "PIN dengan benar untuk menutup Aplikasi ini..!!!");
            angka.requestFocus();
        } else {
            if (Sequel.cariInteger("select count(-1) from antrian_hak_akses where pin_admin_utama='" + angka.getText() + "'") > 0) {
                int jawab = JOptionPane.showConfirmDialog(null, "Yakin anda mau keluar dari program ini...????", "Konfirmasi", JOptionPane.YES_NO_OPTION);
                if (jawab == JOptionPane.YES_OPTION) {
                    System.exit(0);
                }
            } else {
                label_pesan.setText("Angka PIN yang anda masukkan salah, silahkan ulangi "
                        + "lagi dengan benar untuk menutup Aplikasi ini (Hanya Admin Utama)..!!!");
                angka.requestFocus();
            }
        }
    }//GEN-LAST:event_BtnCloseAppActionPerformed

