    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        if (pinLogin.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Silahkan masukkan PIN dengan benar,..!!!");
            pinLogin.requestFocus();
        } else if (Sequel.cariInteger("select count(-1) from antrian_hak_akses where pin_petugas='" + pinLogin.getText() + "'") > 0) {
            WindowPengaturan.setSize(992, 589);
            WindowPengaturan.setLocationRelativeTo(PanelUtama);
            WindowPengaturan.setVisible(true);
            label_pesan.setText("Apa yang anda pikirkan...!!!");
            angka.setText("");
            BtnNonAktif.requestFocus();
            BtnKertas.setEnabled(true);
            BtnSetUmum.setEnabled(false);
            BtnSetBPJS.setEnabled(false);
            BtnSetInap.setEnabled(false);
            BtnSetKhusus.setEnabled(false);

            BtnResUmum.setEnabled(false);
            BtnResBPJS.setEnabled(false);
            BtnResInap.setEnabled(false);
            BtnResKhusus.setEnabled(false);
            logoutActionPerformed(null);
        } else if (Sequel.cariInteger("select count(-1) from antrian_hak_akses where pin_admin_utama='" + pinLogin.getText() + "'") > 0) {
            WindowPengaturan.setSize(992, 589);
            WindowPengaturan.setLocationRelativeTo(PanelUtama);
            WindowPengaturan.setVisible(true);
            label_pesan.setText("Apa yang anda pikirkan...!!!");
            angka.setText("");
            BtnNonAktif.requestFocus();
            BtnKertas.setEnabled(true);
            BtnSetUmum.setEnabled(true);
            BtnSetBPJS.setEnabled(true);
            BtnSetInap.setEnabled(true);
            BtnSetKhusus.setEnabled(true);

            BtnResUmum.setEnabled(true);
            BtnResBPJS.setEnabled(true);
            BtnResInap.setEnabled(true);
            BtnResKhusus.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(null, "PIN yang anda masukkan salah,..!!!");
            pinLogin.setText("");
            pinLogin.requestFocus();
            WindowPengaturan.dispose();
        }
    }//GEN-LAST:event_okActionPerformed

